package cn.bdqn.tangcco.entity;

public class GradeName {
    private Integer granamId;

    private Integer maxNum;

    private Integer schoolId;

    private Integer termId;

    private Integer gratypId;

    public Integer getGranamId() {
        return granamId;
    }

    public void setGranamId(Integer granamId) {
        this.granamId = granamId;
    }

    public Integer getMaxNum() {
        return maxNum;
    }

    public void setMaxNum(Integer maxNum) {
        this.maxNum = maxNum;
    }

    public Integer getSchoolId() {
        return schoolId;
    }

    public void setSchoolId(Integer schoolId) {
        this.schoolId = schoolId;
    }

    public Integer getTermId() {
        return termId;
    }

    public void setTermId(Integer termId) {
        this.termId = termId;
    }

    public Integer getGratypId() {
        return gratypId;
    }

    public void setGratypId(Integer gratypId) {
        this.gratypId = gratypId;
    }
}